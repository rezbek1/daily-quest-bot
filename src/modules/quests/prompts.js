/**
 * Случайный уровень боли по теме
 */
function getPainLevel(theme) {
  const ranges = {
    light:   [1, 5],
    black:   [5, 8],
    venture: [7, 10],
  };
  const [min, max] = ranges[theme] || ranges.black;
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

